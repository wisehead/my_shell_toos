#上面都是把某个进制的数转换为 10 进制的，如果要进行任意进制之间的转换还是 bc 比较灵活，因为它可以直接用 ibase 和 obase 分别指定进制源和进制转换目标。
#!/bin/sh 
echo "obase=10;ibase=8;11" | bc -l
#9

echo $((8#11))
#9
